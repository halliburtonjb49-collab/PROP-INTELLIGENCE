create or replace function public.match_player_stretches(query_embedding vector(16), match_count int default 5)
returns table (id bigint, player_id text, market text, next_game_value double precision, similarity double precision)
language sql stable as $$
  select p.id, p.player_id, p.market, p.next_game_value, 1 - (p.embedding <=> query_embedding) as similarity
  from public.player_stretch_embeddings p order by p.embedding <=> query_embedding limit match_count;
$$;

create or replace function public.match_player_stretches_filtered(
  query_embedding vector(16), query_sport text, query_market text, match_count int default 5
)
returns table (id bigint, player_id text, market text, next_game_value double precision,
               similarity double precision, context jsonb, occurred_at timestamptz)
language sql stable as $$
  select p.id, p.player_id, p.market, p.next_game_value,
         1 - (p.embedding <=> query_embedding) as similarity, p.context, p.occurred_at
  from public.player_stretch_embeddings p
  where p.sport = query_sport and p.market = query_market and p.next_game_value is not null
  order by p.embedding <=> query_embedding limit match_count;
$$;

