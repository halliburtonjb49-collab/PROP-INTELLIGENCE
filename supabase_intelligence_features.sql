-- Prop Intelligence contextual analytics, pgvector analogs, sentiment, and compound alerts.
create extension if not exists vector;

create table if not exists public.player_stretch_embeddings (
  id bigint generated by default as identity primary key,
  sport text not null,
  player_id text not null,
  market text not null,
  stretch_values jsonb not null,
  embedding vector(16) not null,
  next_game_value double precision,
  context jsonb not null default '{}'::jsonb,
  occurred_at timestamptz not null,
  created_at timestamptz not null default now()
);
create index if not exists player_stretch_embedding_hnsw
  on public.player_stretch_embeddings using hnsw (embedding vector_cosine_ops);
create unique index if not exists player_stretch_natural_key
  on public.player_stretch_embeddings(sport, player_id, market, occurred_at);

create table if not exists public.prop_engagement_events (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id) on delete set null,
  prop_id text not null,
  action text not null check (action in ('VIEW','SEARCH','CLICK','WATCHLIST','PICK_OVER','PICK_UNDER')),
  created_at timestamptz not null default now()
);
create index if not exists prop_engagement_recent_idx on public.prop_engagement_events(prop_id, created_at desc);

create table if not exists public.compound_prop_alerts (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  logic text not null default 'ALL' check (logic in ('ALL','ANY')),
  conditions jsonb not null,
  enabled boolean not null default true,
  last_triggered_at timestamptz,
  created_at timestamptz not null default now()
);
create table if not exists public.alert_deliveries (
  id uuid primary key default gen_random_uuid(),
  alert_id uuid not null references public.compound_prop_alerts(id) on delete cascade,
  user_id uuid not null references auth.users(id) on delete cascade,
  fingerprint text not null unique, snapshot jsonb not null,
  delivered_at timestamptz not null default now(), read_at timestamptz
);
create index if not exists alert_deliveries_user_idx on public.alert_deliveries(user_id, delivered_at desc);

